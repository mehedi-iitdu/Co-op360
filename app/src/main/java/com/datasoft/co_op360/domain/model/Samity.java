public class Samity {

    private String id;

    private String workingAreaId;

    private String name;

    private String code;

    private String samityDay;

    private String type;

    private String lat;

    private String lng;

    private String isdo;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getWorkingAreaId() {
        return workingAreaId;
    }

    public void setWorkingAreaId(String workingAreaId) {
        this.workingAreaId = workingAreaId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getSamityDay() {
        return samityDay;
    }

    public void setSamityDay(String samityDay) {
        this.samityDay = samityDay;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getLat() {
        return lat;
    }

    public void setLat(String lat) {
        this.lat = lat;
    }

    public String getLng() {
        return lng;
    }

    public void setLng(String lng) {
        this.lng = lng;
    }

    public String getIsdo() {
        return isdo;
    }

    public void setIsdo(String isdo) {
        this.isdo = isdo;
    }
}
